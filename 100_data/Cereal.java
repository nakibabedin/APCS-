public class Cereal {
    String name;
    double calories;
    double protein;
    double fat;
    double sodium;
    double fiber;
    double carbs;
    double sugar;
    double potassium;
    double vitamins;
    double shelf;
    double weight;
    double cups;
    double ratings;

    public Cereal(
      String name,
      double calories,
      double protein,
      double fat,
      double sodium,
      double fiber,
      double carbs,
      double sugar,
      double potassium,
      double vitamins,
      double shelf,
      double weight,
      double cups,
      double ratings)
      {
        this.name = name;
        this.calories = calories;
        this.protein = protein;
        this.fat = protein;
        this.sodium = sodium;
        this.fiber = fiber;
        this.carbs = carbs;
        this.sugar = sugar;
        this.potassium = potassium;
        this.vitamins = vitamins;
        this.shelf = shelf;
        this.weight = weight;
        this.cups = cups;
        this.ratings = ratings;
    }
}
